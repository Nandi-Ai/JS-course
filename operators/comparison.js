let x =1;


//Relational
console.log(x > 0);
console.log(x >= 1);
console.log(x < 1);

// Equality
// Strict Equality
console.log("Is x equal to 1? ", x === 1 );
console.log("Is x not equal to 1? ", x !== 1 );
console.log("Is \'1\'  equal to 1? ", '1' === 1 );


// Lose Equality
console.log(1 == 1);
console.log('1' == 1);
console.log("is true equal 1", true == 1);
